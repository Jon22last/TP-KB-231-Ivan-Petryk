#Виконати пошук коренів квадратного рівняння враховуючи значення дискримінанту
a = float(input("Введіть a: "))
b = float(input("Введіть b: "))
c = float(input("Введіть c: "))

D = b**2-4*a*c
print(f"Дискримінант: {D}")

if D > 0:
    x1 = (-b + D**0.5) / (2*a)
    x2 = (-b - D**0.5) / (2*a)
    print(f"Рівняння має два корені: x1 = {x1}, x2 = {x2}")
elif D== 0:
    x = -b / (2*a)
    print(f"Рівняння має один корінь: x = {x}")
else:
    print(f"Рівняння не має дійсних коренів")


